# int() function

# Example of calling the int() function
user_age = int("99")

# Name: int
# Arguments: A thing to be converted to an integer
# Side Effects: None
# Return Value: An integer 




# str() function

# Example of calling the str() function
user_age = str(100)

# Name: str
# Arguments: A thing to be converted to a string
# Side Effects: None
# Return Value: A string 




# bool() function

# Example of calling the bool() function

is_true = bool(1)

# Name: bool
# Arguments: A thing to be evaluated for its truthiness
# Side Effects: None
# Return Value: True or False



